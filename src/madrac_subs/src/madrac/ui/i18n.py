# ── Public API ────────────────────────────────────────────────────


def _(text: str) -> str:
    """Translate a Spanish string to the current UI language."""
    if not _STRINGS:
        return text
    return _STRINGS.get(text, text)


def setup(lang_code: str) -> None:
    """Set the current UI language."""
    global _LANG_CODE, _STRINGS
    lang_code = lang_code or "es"
    _LANG_CODE = lang_code
    if lang_code == "es":
        _STRINGS = {}
    else:
        _STRINGS = _LANGUAGES.get(lang_code, {})
    logger.info("UI language set to '%s' (%d strings)", lang_code, len(_STRINGS))


def current_language() -> str:
    """Return the current UI language code."""
    return _LANG_CODE


def detect_system_language() -> str:
    """Detect the Windows display language and return a supported language code.

    Uses GetUserDefaultUILanguage() on Windows, falls back to locale detection.

    Returns:
        Two-letter language code (es, en, fr, pt, de, it, ja, zh).
        Defaults to 'en' if detection fails.
    """
    # Map Windows LANGIDs → our language codes
    _LANGID_MAP = {
        0x0409: "en",  # en-US
        0x040A: "es",  # es-ES
        0x0C0A: "es",  # es-MX
        0x080A: "es",  # es-AR
        0x040C: "fr",  # fr-FR
        0x080C: "fr",  # fr-BE
        0x0C0C: "fr",  # fr-CA
        0x0416: "pt",  # pt-BR
        0x0816: "pt",  # pt-PT
        0x0407: "de",  # de-DE
        0x0807: "de",  # de-CH
        0x0C07: "de",  # de-AT
        0x0410: "it",  # it-IT
        0x0810: "it",  # it-CH
        0x0411: "ja",  # ja-JP
        0x0804: "zh",  # zh-CN
        0x0C04: "zh",  # zh-HK
        0x0404: "zh",  # zh-TW
    }

    # Try Windows API first
    try:
        import ctypes
        lang_id = ctypes.windll.kernel32.GetUserDefaultUILanguage()
        code = _LANGID_MAP.get(lang_id)
        if code:
            logger.debug("Detected system language: LANGID 0x%04X → '%s'", lang_id, code)
            return code
    except Exception as exc:
        logger.debug("Windows language detection failed: %s", exc)

    # Fallback: locale.getdefaultlocale()
    try:
        full_code, _ = locale.getdefaultlocale()
        if full_code and len(full_code) >= 2:
            code = full_code[:2]
            logger.debug("Detected system language via locale: '%s'", code)
            return code
    except Exception as exc:
        logger.debug("Locale detection failed: %s", exc)

    return "en"
